    public static function cities(?string $province = null): array
    {
        $all = self::provinces();

        if ($province !== null) {
            return $all[$province] ?? [];
        }

        return array_values(array_merge(...array_values($all)));
    }

    public static function provinceList(): array
    {
        return array_combine(
            array_keys(self::provinces()),
            array_keys(self::provinces()),
        );
    }
