            <li><strong>Eliminar</strong> sus datos cuando ya no sean necesarios.</li>
            <li><strong>Oponerse</strong> al tratamiento de sus datos.</li>
            <li><strong>Solicitar la portabilidad</strong> de sus datos a otro responsable.</li>
            <li><strong>Solicitar la suspensión</strong> del tratamiento cuando corresponda.</li>
        </ul>
        <p>Para ejercer cualquiera de estos derechos, escríbanos a <a href="mailto:servicioalcliente@paraiso.com.ec">servicioalcliente@paraiso.com.ec</a> indicando su nombre, el derecho que desea ejercer y los datos relacionados. Responderemos a su solicitud en un plazo máximo de 15 días hábiles.</p>

