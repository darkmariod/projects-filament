        <p>El tratamiento de sus datos se basa en su consentimiento expreso, manifestado al aceptar la casilla de verificación durante el registro de la garantía. Puede retirar su consentimiento en cualquier momento contactándonos.</p>

        <h2>Plazo de conservación</h2>
        <p>Conservamos sus datos personales durante la vigencia de la garantía del producto más cinco años adicionales para cumplir con obligaciones contables y legales en Ecuador. Una vez vencido este plazo, sus datos serán eliminados de forma segura.</p>

        <h2>Destinatarios y transferencias</h2>
        <p>No compartimos sus datos personales con terceros ni realizamos transferencias internacionales de datos. La información se almacena en servidores ubicados en Ecuador.</p>

